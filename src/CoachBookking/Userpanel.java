    private void btnBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBookActionPerformed
        String Name, NIC, Start, Destination, Date , Tickets;
        
        Name =txtName.getText();
        NIC =txtNIC.getText();
        Start = txtStart.getText();
        Destination=txtDest.getText();
        Date =txtDate.getText();
        Tickets=txtTickets.getText();
        
        Connector N = new Connector();
        Connection C =N.connect();
        if(Name != null && NIC != null && Start != null && Destination != null && Date != null && Tickets != null)
        {
        try
        {
        Statement S = C.createStatement();
        int A=S.executeUpdate("INSERT INTO BOOKED VALUES('"+Date+"','"+Name+"','"+NIC+"','"+Start+"','"+Destination+"','"+Tickets+"')");
        if(A==1)
            {
            JOptionPane.showMessageDialog(null, "Successfully Booked");
            }
        }
        catch(SQLException m)
        {}}
        
        else
        {
         JOptionPane.showMessageDialog(null, "Some Values are missing");
        }
    }//GEN-LAST:event_btnBookActionPerformed

