    private void btnfilterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnfilterActionPerformed
        try{
        Connector N = new Connector();
        Connection C =N.connect();
        Statement S=C.createStatement();
        ResultSet R;
        String price , destination;
        price =txtmin.getText();
        destination=txtdest.getText();
        
        if(price != null && destination != null){
        R=S.executeQuery("SELECT DATE_ , STARTING_POINT  , DESTINATION_POINT , PRICE FROM ADMIN WHERE PRICE <'"+price+"' AND DESTINATION_POINT = '"+destination+"' ");
        view_table.setModel(DbUtils.resultSetToTableModel(R));
        }
         if(price == null && destination != null){
        R=S.executeQuery("SELECT DATE_ , STARTING_POINT  , DESTINATION_POINT , PRICE FROM ADMIN WHERE DESTINATION_POINT = '"+destination+"' ");
        view_table.setModel(DbUtils.resultSetToTableModel(R));
         }
          if(price != null && destination == null){
        R=S.executeQuery("SELECT DATE_ , STARTING_POINT  , DESTINATION_POINT , PRICE FROM ADMIN WHERE PRICE <'"+price+"'");
        view_table.setModel(DbUtils.resultSetToTableModel(R));
          }
        
        
        }
         catch(SQLException S){}
                   
    }//GEN-LAST:event_btnfilterActionPerformed

